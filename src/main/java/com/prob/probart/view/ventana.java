    private void jButton7ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton7ActionPerformed
        // TODO add your handling code here:
        String title = jTextField1.getText();
        Optional<Pelicula> optionalPelicula = peliculaRepositorio.findById(title);
        if (!optionalPelicula.isPresent()) {
            try {
                if(title.equals("Título")) {
                    jSeparator1.setBackground(Color.RED);
                    jTextField1.setForeground(Color.RED);
                }
                if(jTextArea1.getText().equals("Resumen")) {
                    jSeparator2.setBackground(Color.RED);
                    jTextArea1.setForeground(Color.RED);
                }
                if(jTextField3.getText().equals("Año")) {
                    jSeparator3.setBackground(Color.RED);
                    jTextField3.setForeground(Color.RED);
                }
                if(jTextField4.getText().equals("Director")) {
                    jSeparator4.setBackground(Color.RED);
                    jTextField4.setForeground(Color.RED);
                }
                getDataFormPelicula(new Pelicula());
                jLabel12.setForeground(Color.GREEN);
                jLabel12.setText("Se registró correctamente la pelicula " + jTextField1.getText());
                jButton7.setVisible(false);
                jButton25.setVisible(true);
            }
            catch(Exception e) {
                jLabel12.setForeground(Color.RED);
                jLabel12.setText("Ha ocurrido un error. Por favor, intente nuevamente.");
            }
        } else {
            jLabel12.setForeground(Color.RED);
            jLabel12.setText("La película " + title + " ya se encuentra registrada.");
        }
    }//GEN-LAST:event_jButton7ActionPerformed

