    private void getDataFormPelicula(Pelicula obj) {
        String titulo = jTextField1.getText();
        String resumen = jTextArea1.getText();
        Long anio = Long.parseLong(jTextField3.getText());
        String director = jTextField4.getText();
              
        Pelicula pelicula = obj;
        pelicula.setPelTitulo(titulo);
        pelicula.setPelResumen(resumen);
        pelicula.setPelAnio(anio);
        pelicula.setPelDirector(director);
        
        peliculaRepositorio.save(pelicula);
    }
    
    private void getDataFormSeries(Series obj) {
        String titulo = jTextField5.getText();
        Long temporadas = Long.parseLong(jTextField6.getText());
        Long episodios = Long.parseLong(jTextField6.getText());
              
        Series serie = obj;
        serie.setSerTitulo(titulo);
        serie.setSerTemporadas(temporadas);
        serie.setSerEpisodios(episodios);
        
        seriesRepositorio.save(serie);
    }
    
